while True:
    string=input("please input a string:")
    count=0
    for i in range(0, len(string)):
        if string[i]=="a":
            count +=1
        elif string[i]=="e":
            count +=1
        elif string[i]=="i":
            count +=1
        elif string[i]=="o":
            count +=1
        elif string[i]=="u":
            count +=1
    print("The number of vowel is :", count)
        
        
        
    
            
            
