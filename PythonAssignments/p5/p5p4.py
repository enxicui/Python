x=int(input('Please input an integer:',))
if x==0:
    print('x=0')
elif 0<x<=20:
    print('0<x<=20')
elif 20<x<=40:
    print('20<x<=40')
elif 40<x<=60:
    print('40<x<=60')
elif 60<x<=80:
    print('60<x<=80')
elif 80<x<=100:
    print('80<x<=100')
elif x>100:
     print('x>100')
else:
    print('x<0')
    
